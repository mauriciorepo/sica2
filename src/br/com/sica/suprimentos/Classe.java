package br.com.sica.suprimentos;

public class Classe {

	private String idprodutoclasse;
	private String descricao;
	private String data;
	private String cod;
	
	
	public String getDescricao() {
		return descricao;
	}
	public void setDescricao(String descricao) {
		this.descricao = descricao;
	}
	public String getData() {
		return data;
	}
	public void setData(String data) {
		this.data = data;
	}
	public String getCod() {
		return cod;
	}
	public void setCod(String cod) {
		this.cod = cod;
	}
	public void setIdprodutoclasse(String idprodutoclasse) {
		this.idprodutoclasse = idprodutoclasse;
	}
	public String getIdprodutoclasse() {
		return idprodutoclasse;
	}
	
}
