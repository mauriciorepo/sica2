package br.com.sica.financeiro;

public class PlanodeContas {

	private String nome;
	private String subtipo;
	private String tipo;
	private int idplanodecontas;
	public String getNome() {
		return nome;
	}
	public void setNome(String nome) {
		this.nome = nome;
	}
	public String getSubtipo() {
		return subtipo;
	}
	public void setSubtipo(String subtipo) {
		this.subtipo = subtipo;
	}
	public String getTipo() {
		return tipo;
	}
	public void setTipo(String tipo) {
		this.tipo = tipo;
	}
	public int getIdplanodecontas() {
		return idplanodecontas;
	}
	public void setIdplanodecontas(int idplanodecontas) {
		this.idplanodecontas = idplanodecontas;
	}
	
}
